    // Fungsi untuk mengontrol slideshow pada halaman utama
    $(document).ready(function() {
        // Variabel untuk indeks slide
        var slideIndex = 0;
        // Memanggil fungsi untuk menampilkan slide pertama kali
        showSlides();

        // Fungsi untuk menampilkan slide secara otomatis
        function showSlides() {
            // Mengambil elemen-elemen dengan kelas "slide"
            var slides = document.getElementsByClassName("slide");
            // Menyembunyikan semua slide
            for (var i = 0; i < slides.length; i++) {
                slides[i].style.display = "none";
            }
            // Menambahkan indeks slide
            slideIndex++;
            // Jika indeks slide melebihi jumlah slide, kembalikan ke slide pertama
            if (slideIndex > slides.length) {slideIndex = 1}
            // Menampilkan slide yang sesuai dengan indeks
            slides[slideIndex-1].style.display = "block";
            // Mengatur waktu untuk menampilkan slide berikutnya setiap 3 detik
            setTimeout(showSlides, 3000); // Change image every 3 seconds
        }
    });
</script>

